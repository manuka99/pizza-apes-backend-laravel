    /**
     * Handle the Product "updated" event.
     *
     * @param  \App\Models\Product  $product
     * @return void
     */
    public function updated(Product $product)
    {
        if ($product->isDirty('type')) {
            // type has changed
            $new_type = $product->type;
            $old_type = $product->getOriginal('type');

            if ($old_type === "bundle")
                $product->options()->delete();

            if ($old_type === "variant") {
                $product->options()->delete();
                $product->productVarients()->delete();
            }

            if ($new_type === "variant")
                $product->productVarients()->delete();
        }
    }

